<h1>Control Biológico de plagas</h1>
<p>En este apartado se detalla una alternativa del uso de productos fitosanitarios para el control de plagas, mediante la lucha biológica con una serie de depredadores naturales, como son los ácaros, insectos, mamíferos, reptiles y aves. Una de las principales características del control biológico es la especificidad, quizás mucho más que las sustancias empleadas en el control químico (Hidalgo, 2011). </p>
<p>Con medidas sencillas y de bajo coste es posible favorecer la presencia de depredadores naturales, aumentando la biodiversidad en el viñedo y aprovechando los propios mecanismos de equilibrio biológico existentes en los ecosistemas (García Sánchez & Román Cabrera, 2013).</p>
<p>Hay algunos elementos estructurales claves que deben establecerse para el control biológico, como la presencia de vegetación refugio: cubiertas vegetales, setos, islas de vegetación o masas de arbolados cercanos, así como elementos tradicionales de paredes de piedra, que supondrán refugio a predadores terrestres. También se recomienda plantar en el viñedo flora autóctona como romero, rosa silvestre, ortigas, lavanda, etc., que servirán no solo de refugio a insectos, ácaros, réptiles, etc., sino que también favorecerán la creación de un microclima adecuado, además de fuente de alimento.</p>
<p>Debido a que en las poblaciones forestales jóvenes la escasez de huecos naturales puede limitar la nidificación (Martín Bernal et al., 2005), es necesario instalar estructuras tipo cajas-nido, posaderos y/o bebederos, creadas por nosotros para proporcionar refugio a aves y mamíferos, situándolas sobre árboles, postes, etc. Puede que haya que pedir permiso para solicitar autorización según la especie que queramos introducir.</p>

