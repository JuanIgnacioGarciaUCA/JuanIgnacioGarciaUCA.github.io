<h1 id="bacteria2">NECROSIS BACTERIANA (<i>Xylophilus ampelina</i>)</h1>
<p></p>
<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p><i>Xylophilus ampelina</i>, Panagopoulos y Willens.</p>
<p>Es una enfermedad vascular crónica de la vid.</p>
<p></p>
<p style="font-size:20px;">•	Ciclo biológico</p>
<p>Es una bacteria que mide hasta 3,2 x 0,8 µm, aisladas o en parejas, emplean un flagelo para moverse, son aerobias, producen una pigmentación amarilla, insolubles en agua.</p>
<p>Durante el invierno las bacterias se mantienen en la parte aérea de la vid, la contaminación tiene lugar por las heridas de poda. Una vez se produce la contaminación a partir de plantas enfermas, las bacterias se difunden por la vid por vía vascular. La enfermedad puede ser diseminada por las herramientas de poda o maquinarias podadoras, trituradoras y cosechadoras.</p>
<p>El desarrollo de la enfermedad se ve favorecido en primaveras frías y lluviosas. La bacteria es expulsada al exterior con los lloros, infectando yemas y brotes.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>Los síntomas aparecen principalmente en primavera y mediados de verano. Fluctuando más o menos en función de las cepas bacterianas, variedad de uva, técnicas de cultivo y condiciones climáticas.</p>
<p>Las yemas afectadas brotan con dificultad dando lugar a brotes raquíticos y con retraso, pudiendo abortar y marchitarse gradualmente.</p>
<p>En la base de los sarmientos se visualizan manchas negras violáceas, que suelen tener un margen húmedo y aceitoso, evolucionando a heridas profundas. Los sarmientos no lignifican adecuadamente quedando bandas amarillo verdosas longitudinales y arqueándose hacia el suelo.</p>
<p>Las hojas de la base de los pámpanos presentan pequeñas manchas de hasta 3 mm rojizas, con un contorno amarillento.  </p>
<p>En los pedúnculos y el raspón ocurre lo mismo, además las flores adquieren una consistencia dura, de color rojizo, con un corrimiento muy acentuado y/o secado.</p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p></p>
<p><strong> Cultural:</strong>  aplicar medidas preventivas, emplear material vegetal sano para nuevas plantaciones, eliminar las plantas enfermas y restos de poda, podar en máximo periodo de reposo vegetativo empezando por las sanas y terminando por las enfermas, desinfectar las herramientas de poda, no abusar de abonos nitrogenados.</p>
<p>Si la enfermedad no ha penetrado en la planta, el caldo bordelés y productos cúpricos son muy eficaces.</p>
<p>Se ha ensayado la eficacia de la termoterapia, tratado los sarmientos agostados en agua a 50ºC durante 45 minutos para obtener material vegetal libre de bacterias para las nuevas plantaciones, pero han de plantarse en un periodo máximo de 48h.</p>
<p></p>
<p><strong> Químico:</strong>  se recomienda emplear productos químicos autorizados de forma específica en el Registro de productos fitosanitarios del MAPAMA</p>
