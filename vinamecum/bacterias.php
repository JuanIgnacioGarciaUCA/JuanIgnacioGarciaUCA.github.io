<h1 id="bacteria1">ENFERMEDAD DE PIERCE (<i>Xylella fastidiosa</i>)</h1>
<p></p>
<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p><i>Xylella fastidiosa</i> ST1, ST2, ST4, ST18 Y ST41.</p>
<p>Bacteria con enorme potencial patógeno. Las primeras infecciones en Europa fueron en octubre del 2013. Se han tomado medidas para evitar su dispersión y erradicarla</p>
<p></p>
<p style="font-size:20px;">•	Vector de la enfermedad</p>
<p>La bacteria se transmite de una planta a otra mediante insectos voladores, de las familias: <i>Aprophoriadae, Cicadelidae y Cercopidae</i>. Todos ellos son Hemípteros chupadores de savia que pueden infectar plantas sanas. También se transmite la enfermedad por la propagación de material infectado (viveros, invernaderos, etc.), las zonas con mayor riesgo son las Mediterráneas.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>La bacteria penetra mediante vectores (insectos) dentro del xilema de la vid y se multiplica en el interior de los vasos, obstruyendo el flujo de savia bruta, agua y sales minerales.</p>
<p>Los primeros síntomas de la enfermedad son foliares, apareciendo principalmente durante los periodos calurosos y secos del verano, o cuando las vides sufren estrés hídrico, produciéndose el secado parcial de las hojas. Las lesiones se manifiestan desde los bordes de las hojas hacia dentro del limbo, extendiéndose progresivamente o en sectores, adquiriendo coloraciones marrones, amarillas, con los bordes rojizos.</p>
<p>Los pámpanos presentas zonas marrones. Las cepas afectadas, en las siguientes temporadas producirán brotes cloróticos y atrofiados, produciéndose la muerte de la cepa a año o dos años de la infección.</p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p></p>
<p><strong> Cultural:</strong>  evitar el comercio de material vegetal de plantas infectadas, las plantas deben estar oficialmente autorizadas y con pasaporte fitosanitario. </p>
<p>Destruir las plantas afectadas, realizar tratamientos preventivos con insecticidas contra el vector (<i>Aprophoriadae, Cicadelidae y Cercopidae</i>).</p>
<p></p>
<p><strong> Químico:</strong>  desafortunadamente no hay medios de control para curar la enfermedad. </p>
