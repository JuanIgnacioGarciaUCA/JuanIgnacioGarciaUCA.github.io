<h1 id="hongo8">YESCA (<i>Sterum hirsutum, Phaeomoniella chlamydosipora y Phellinus ignarius</i>)</h1>
<p>Clase Eumicetos. Subclase Basidiomicetos. Familia <i>Teloforaceae</i> y <i>Poliporácae</p>
<p></p>
<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p><i>Sterum hirsutum</i> Willd., <i>Phaeomoniella chlamydosipora</i> W. Gams, Crous, M. J. Wingf & Mungnai, y <i>Phellinus ignarius</i> E. Fungorum.</p>
<p></p>
<p style="font-size:20px;">•	Ciclo biológico</p>
<p>Los hongos que producen la enfermedad penetran por las heridas de poda, hasta alcanzar el cilindro central del brazo o el tronco, gracias a la segregación de una oxidasa, que actúa sobre los taninos, produciendo una zona amarilla que más tarde se vuelve negra.</p>
<p>El hongo avanza por el interior de los brazos y el tronco de la vid, sin llegar a salir al exterior, destruyendo los vasos conductores, secando así el brazo o la totalidad de la cepa si el hongo alcanza el tronco principal.</p>
<p>En regiones de clima cálido pueden proliferar pequeñas setas perpendiculares en la corteza de la madera ya muerta, produciendo esporas que propagan la enfermedad. </p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>La Yesca presenta síntomas en los órganos verdes, con coloraciones anómalas debido a la presencia de trastornos vasculares, relacionados con la falta de circulación de la savia en los tejidos y desequilibrios de evapotranspiración si las temperaturas son muy altas, y necrosis de la madera.</p>
<p>Los síntomas comienzan por uno o varios brazos, por los que penetra el hongo. Las hojas dificultan la circulación en los periodos secos o después de la floración, se decoloran entre los nervios y bordes, adquiriendo tonos amarillentos en las variedades blancas y rojizos en las tintas, que se unen y secan en el centro, con un aspecto de mosaico, produciendo la defoliación de las hojas y que los racimos se sequen tomando coloraciones violáceas.</p>
<p>En los brotes del año, pueden producirse desecaciones bruscas desde el extremo hacia la base del brote, o mal agostamiento en otoño. </p>
<p>Si se realizan cortes transversales en la madera vieja se puede apreciar la necrosis de los tejidos, que va descendiendo progresivamente, de arriba hacia abajo, con una consistencia blanda.</p>
<p>También en casos donde las temperaturas son muy altas, se puede producir la apoplejía de forma rápida y secar total o parcialmente la cepa.</p>
<p>Las manifestaciones pueden variar de un año para otro, siendo muy intensas uno y apenas perceptible el siguiente, produciéndose la muerte de un brazo de la planta o de toda ella.</p>
<p>En las bayas se produce un retraso de la maduración, marchitez y mancas marrones o violáceas en la superficie.</p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p></p>
<p><strong> Cultural:</strong> antes de la plantación emplear vides controladas y desinfectadas con certificado, no emplear en injertación sarmientos contaminados, controlar la calidad de la soldadura patrón-portainjerto, no guardar demasiado tiempo los barbados en frío, ni fertilizar en exceso.</p>
<p>En la plantación: No dejar las raíces demasiado tiempo sumergidas en agua antes de la plantación. Riego de nueva plantación adecuadamente, evitar suelos muy vigorosos.</p>
<p>Después de la plantación: Elegir un modelo de conducción y densidad aérea que permita la formación adecuada de brazos, formar correctamente los troncos de vides jóvenes, prevenir condiciones que puedan causar estrés a las plantas durante al menos los 4 primeros años de crecimiento, favorecer el sistema radicular y no tanto el aéreo, evitar podas defectuosas con grandes heridas y rebajes, podas verticales; evitar podas temprana, proteger las heridas de poda con “masilla”. </p>
<p>Eliminar los restos de poda con infección. Desinfectar los útiles de poda, podando las cepas afectadas en último lugar.</p>
<p>Si hay afección podar hasta llegar a la madera sana, podar las cepas afectadas las últimas, abrir la cruz de la cepa con un hacha y colocar una cuña para que la herida permanezca abierta, con el fin de provocar la desecación del micelo y evitar que el hongo no se desarrolle, marcar las cepas enfermas.</p>
<p></p>
<p><strong> Biológico:</strong>  mediante el hongo antagonista del género <i>Trichoderma</i>. </p>
