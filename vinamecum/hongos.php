<h1 id="hongo3">ENFERMEDAD DE PETRI (<i>P. chlamydospora, P. aleophilum, P.inflatipes, Botryosphaeria, Phomopsis, Eutypa, Verticillium, Phytophthora y Pythium</i>) </h1>
<p>Clase Eumicetos. Subclase Basidiomicetos. Familia <i>Agaricacae</i></p>
<p></p>
<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p>La enfermedad la producen la invasión de diferentes hongos: <i>Phamaeomoniella chlamydospora, Phamaeoacremonium aleophilum, Phamaeoacremonium inflatipes, Botryosphaeria, Phomopsis, Eutypa, Verticillium, Phytophthora</i> y <i>Pythium</i>. </p>
<p></p>
<p style="font-size:20px;">•	Ciclo biológico</p>
<p><i>Phamaeomoniella chlamydospora</i>: se reproduce sexualmente y se transmite por madera infectada, desde el suelo o por diseminación aérea.</p>
<p><i>Phamaeoacremonium aleophilum</i>: la transmisión se realiza por madera infectada y suelo, existiendo también una contaminación aérea.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>Hay varios síntomas en la vid, depende de la especie de hongo y área de producción.</p>
<p>Las viñas manifiestan un crecimiento lento, mortalidad de las yemas y brotes, entrenudos cortos, necrosis y manchas en las hojas, síntomas de clorosis y desecación de los brotes, tomando un aspecto de decaimiento. También se reduce el diámetro del portainjerto, pudiendo quebrarse con facilidad el callo de soldadura. </p>
<p>El tronco puede aplanarse y producirse una fisura longitudinal. Pueden aparecer exudaciones de pequeñas gotas color miel oscura, que recuerdan al alquitrán en la sección transversal del tallo o raíces, que obstruye el xilema. En el tronco aparecen vetas rojizas.</p>
<p>Las plantas afectadas suelen morir a los 5 años de su plantación.</p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p></p>
<p><strong> Cultural:</strong>  empleo de material vegetal sano, prevenir condiciones que puedan causar estrés a las plantas durante al menos los 4 primeros años de crecimiento, favorecer el sistema radicular y no tanto el aéreo. Evitar encharcamientos y compactación del terreno. </p>
<p>Evitar podas defectuosas con grandes heridas y rebajes, podas verticales, evitar podas tempranas y realizarlas después de las lluvias, proteger las heridas de poda con “masilla o  protector”, eliminar los restos de poda con infección, desinfectar los útiles de poda, eliminar las partes afectadas y enfermas de los brazos y tronco (hasta 10 cm por debajo de los síntomas). </p>
<p>La utilización de sustancias estimuladoras de autodefensas, que estimulan la biosíntesis de compuestos fenólicos, puede ser bastante eficaz para combatir esta enfermedad.</p>
<p></p>
<p><strong> Químico:</strong> hasta la fecha, no existe un método de lucha efectivo. Se recomienda emplear productos químicos autorizados de forma específica en el Registro de productos fitosanitarios del MAPAMA:  </p>
<p></p>
<p><a href="http://www.mapa.gob.es/es/agricultura/temas/sanidad-vegetal/productos-fitosanitarios/registro/productos/forexi.asp?e=0&plagEfecto=441">http://www.mapa.gob.es/es/agricultura/temas/sanidad-vegetal/productos-fitosanitarios/registro/productos/forexi.asp?e=0&plagEfecto=441</p>
