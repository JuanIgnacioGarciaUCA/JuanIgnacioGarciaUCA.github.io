<h1 id="hongo2">EUTIPIOSIS (<i>Eutypa armeniacae</i>)</h1>
<p>Clase Eumicetos. Subclase Ascomicetos. Familia <i>Valsácea</i></p>
<p></p>
<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p><i>Eutypa armeniacae</i>, Hansf. Y Carter.</p>
<p></p>
<p style="font-size:20px;">•	Ciclo biológico</p>
<p>El ciclo se inicia a partir de las peritecas formadas sobre la madera muerta o atacada, dando lugar a ascosporas, que son arrastradas por el viento o las lluvias. Las ascosporas penetran en la cepa a través de las heridas de poda, necesitando una gran humedad relativa, del 90 por 100 o agua para desarrollarse.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>La Eutypa puede desarrollarse durante varios años en el interior de la cepa antes de que aparezcan los primeros síntomas foliares.</p>
<p>Brazos y tronco: necrosis de la madera, adquiriendo una consistencia dura, con coloración marrón oscuro que contrasta con el blanco de la madera. En las heridas de poda la madera se degrada progresivamente y se rompe fácilmente.</p>
<p>Si el hongo ha penetrado por un brazo, éste presentará raquitismo, crecimiento lento, pámpanos débiles, entrenudos cortos, hojas pequeñas, deformadas y cloróticas, pudiendo presentar manchas de necrosis en casos severos. </p>
<p>Las inflorescencias pueden sufrir corrimiento, o secarse antes de la floración. </p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p><strong> Cultural:</strong> empleo de material vegetal sano, tanto en nueva plantación como de reposición, prevenir condiciones que puedan causar estrés a las plantas durante al menos los 4 primeros años de crecimiento y favorecer el sistema radicular en contra del aéreo. Evitar encharcamientos y compactación del terreno. </p>
<p>Evitar podas defectuosas con grandes heridas y rebajes, podas verticales. Evitar podas tempranas y después de lluvias. Proteger las heridas de poda con “masilla”. Eliminar los restos de poda con infección. Desinfectar los útiles de poda. </p>
<p>Una cepa enferma puede rehacerse dejando brotes de la madera sana del tronco y eliminando el resto.</p>
<p></p>
<p><strong> Biológico:</strong>  mediante el uso de microorganismos antagonistas: <i>Trichoderma spp</i>.  </p>
<p></p>
<p><strong> Químico:</strong>  no hay métodos curativos para erradicar el hongo de la madera. </p>
