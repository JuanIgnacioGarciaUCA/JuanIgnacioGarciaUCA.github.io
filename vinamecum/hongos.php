<h1 id="hongo5">MILDIU (<i>Plasmopara viticola</i>)</h1>
<p>Clase Ficomicetos. Familia <i>Peonosporaceos</i></p>
<p></p>
<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p><i>Plasmopara viticola</i>, Berl. y de Toni.</p>
<p>Se trata de una enfermedad muy conocida y temida por todos los viticultores del mundo.</p>
<p></p>
<p style="font-size:20px;">•	Ciclo biológico</p>
<p>El hongo pasa el invierno en las hojas muertas del suelo bajo la forma de oosporas, forma muy resistente a las condiciones climáticas gracias a dos membranas que las recubren, conservando su poder de infección al menos dos años. Miden entre 25 – 40 micras de diámetro, visibles al microscopio por transparencia a 100 aumentos, con forma globosa, de color amarillo y localizadas normalmente junto a los nervios de la hoja.</p>
<p>En primavera, cuando la temperatura y humedad asciende, las oosporas germinan emitiendo macroconidios, órganos transportadores de las zoosporas. Las zoosporas son gérmenes provistos de cilios para tener movilidad, y una vez entran en contacto con los órganos verdes de la vid germinan a través de los estomas para formar el micelio interno, ramificado y con haustorios, que invaginan las paredes de las células vegetales succionando sus jugos. La zona atacada presenta por el haz una coloración verde claro, denominada “mancha de aceite” y por el envés una pelusilla blanca, formada por los conidióforos que emergen de los estomas, arborescentes, en cuyos extremos están las conidias, que pueden desprenderse con ayuda del viento y el agua formando nuevas colonias.</p>
<p>En otoño, cuando las temperaturas descienden, aparecen sobre las hojas en caída, manchas con forma de mosaico, las cuales contienen los huevos de invierno u oosporas.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>El mildiu se desarrolla exclusivamente en los órganos verdes de la vid, particularmente en los más jóvenes y en fase de crecimiento. </p>
<p>Hojas: Se pueden observar dos tipos de lesiones según la época del año. </p>
<p>Manchas circulares, inicialmente translúcidas a aceitosa, con una coloración amarilla que se vuelve necrótica. En el envés se produce una pelusilla blanquecina, pudiendo cubrir casi toda la hoja, secándolas y provocando que se caigan (defoliación de la cepa).</p>
<p>Manchas de forma poligonal necrótica “mosaico de moho”.</p>
<p>Las inflorescencias contaminadas, se deforman, tomando un aspecto de doble curvatura en “S”, el raquis se oscurece, cubriéndose al final de una pelusilla blanquecina si la humedad es alta. </p>
<p>Las bayas recién cuajadas también se oscurecen y se forma una pelusilla blanquecina. Si han alcanzado el tamaño guisante se arrugan y desecan, produciendo una pérdida parcial o total del racimo.</p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p></p>
<p><strong> Cultural:</strong> las técnicas preventivas consisten en emplear variedades menos sensibles, mejorar el drenaje del suelo y reducir las formas invernantes. </p>
<p>Realizar podas en verde: despuntado, desnietado y deshojado después de la floración y cuajado, para favorecer la aireación de los racimos, la penetración y acción de los fungicidas aplicados, no labrar durante la floración.</p>
<p>El sistema tradicional ha conseguido llevar a cabo tratamientos preventivos, en ocasiones con fechas fijas en el calendario, intensificando el número de tratamientos cuando las primaveras son lluviosas. Este sistema implica efectuar demasiados tratamientos y no siempre situados en el mejor momento.</p>
<p>Seguimiento según los momentos del ciclo biológico:</p>
<p>Maduración de las esporas en invierno: valoración cualitativa de la maduración de las esporas. </p>
<p>Inicio de infecciones secundarias: según la temperatura y las horas de rocío valorar la posibilidad del desarrollo del hongo.</p>
<p>Duración del periodo de incubación: el más usado es el Goidanich. <a href="http://agrocabildo.org/publica/Publicaciones/viti_246_L_modelo_mildiu_2009.pdf">http://agrocabildo.org/publica/Publicaciones/viti_246_L_modelo_mildiu_2009.pdf</p>
