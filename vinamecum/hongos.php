<h1 id="hongo1">BLACK-ROT (<i>Guignardia bidwellii</i>)</h1>
<p>Clase Eumicetos. Subclase Ascomicetos. Familia <i>Miscosfaerellacae</i></p>
<p></p>
<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p><i>Guignardia bidwellii</i>, Viala y Ravaz.</p>
<p>Se manifiesta en zonas húmedas y cálidas.</p>
<p></p>
<p style="font-size:20px;">•	Ciclo biológico</p>
<p>Las peritecas durante el invierno van madurando hasta la brotación, en primavera. Con la ayuda de las lluvias las ascosporas contenidas en las peritecas situadas en las hojas bajas germinan, produciendo manchas grisáceas pasando al rojo ladrillo que muy pronto se cubre de puntitos negros, en los cuales se producen las picnidiosporas, encargadas de realizar la contaminación secundaria, sucediéndose varias veces, formando peritecas invernantes.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>El hongo ataca principalmente a todos los órganos jóvenes de la vid en fase de crecimiento activo, especialmente las hojas y los racimos.</p>
<p>Hojas: Los síntomas aparecen primero en las hojas inferiores de la vid por el ambiente húmedo. Van apareciendo pequeñas manchas, generalmente dos semanas después de la infección, ligeramente cloróticas y circulares al inicio. Las manchas van extendiéndose gradualmente, tornándose marrón claro con el borde más oscuro. En condiciones climáticas favorables, aparecen pequeñas estructuras esféricas negras en la superficie de la hoja.</p>
<p>Racimos y bayas: En las bayas se produce el peor daño, sensibles desde el cuajado hasta la maduración. Aparecen decoloraciones, depresiones y manchas que crecen y se vuelven marrones. Las bayas maduras se alteran muy rápidamente, volviéndose negro-azuladas recubriéndose de puntos negros, se arrugan, se desecan y adquieren un sabor desagradable.</p>
<p>En los peciolos, zarcillos y el raspón se producen lesiones necróticas oscuras con puntitos negros. Los tejidos verdes se vuelven marrones y cubren de picnidios. </p>
<p></p>
<p style="font-size:20px;">•	Métodos de control</p>
<p><strong> Cultural:</strong>  la estrategia de protección consiste en impedir el desarrollo del hongo. Se recomienda realizar podas en verde para favorecer la aireación de los racimos y la penetración y acción de los fungicidas, usar variedades menos sensibles, eliminar partes afectadas, en la poda de invierno evitar realizar cortes pegados al tronco para evitar la formación del cono de desecación (siguiendo las carreras en verde y seco del tronco y brazos), y labrar el suelo antes de la brotación para enterrar tejidos enfermos.  </p>
<p></p>
<p><strong> Biológico:</strong>  actualmente no se ha encontrado lucha biológica. </p>
<p></p>
<p><strong> Químico:</strong> en algunos casos la lucha está asociada con la protección preventiva química, se recomienda emplear productos químicos autorizados de forma específica en el Registro de productos fitosanitarios del MAPAMA: </p>
<p></p>
<p><a href="http://www.mapa.gob.es/es/agricultura/temas/sanidad-vegetal/productos-fitosanitarios/registro/productos/forexi.asp?e=0&plagEfecto=32">http://www.mapa.gob.es/es/agricultura/temas/sanidad-vegetal/productos-fitosanitarios/registro/productos/forexi.asp?e=0&plagEfecto=32</a></p>
