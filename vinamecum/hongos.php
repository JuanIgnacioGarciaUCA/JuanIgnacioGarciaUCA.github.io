<h1 id="hongo7">PODREDUMBRE GRIS (<i>Botrytis cinerea</i>)</h1>
<p>Clase Eumicetos. Subclase Ascomicetos. Familia <i>Helotiacae</i></p>
<p></p>
<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p><i>Botrytis cinerea</i>, Pers.</p>
<p>Esta enfermedad se manifiesta en casi todos los viñedos españoles, principalmente en zonas del Norte y costa del Mediterráneo. </p>
<p></p>
<p style="font-size:20px;">•	Ciclo biológico</p>
<p>El hongo se produce al germinar en la superficie de la planta las esporas, este se extiende por el interior de los órganos atacados. Cuando sale al exterior, una vez ha destruido los tejidos parasitados, produce fructificaciones arborescentes, en las cuales se agrupan las conidiosporas en forma de racimos, con formas elípticas y pequeñas, que una vez han madurado se desprenden. </p>
<p>Estas formaciones son hialinas, pero cuando las condiciones climáticas son favorables se vuelven color pardo grisáceo, formando una vellosidad grisácea en las partes infectadas, la cual da el nombre a la enfermedad. </p>
<p>En otoño aparecen en las hojas y los tallos los esclerocios, cuya parte interna es blanca y blanda, y la exterior dura y negra, produciendo en la siguiente primavera conidiosporas para seguir manteniendo y propagando la enfermedad.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>La podredumbre gris puede afectar a todos los órganos verdes de la cepa, pero principalmente a los racimos.</p>
<p>Los síntomas en las hojas se manifiestan en manchas localizadas en el borde, con forma circular a irregular, con aspecto de quemaduras, si además la temperatura y humedad son favorables aparece un polvillo grisáceo.</p>
<p>Los primeros síntomas en brotes y sarmientos son manchas pardas y alargadas cubiertas de una pelusilla gris, que se vuelven negras sobre un fondo blanquecino al final del ciclo vegetativo, agostando mal. Si los ataques son severos pueden perderse brotes, disminuyendo la cosecha.</p>
<p>Los síntomas en los racimos durante el periodo de floración-cuajado aparecen sobre el raspón y las inflorescencias en forma de manchas marrones. Durante el envero y maduración los granos presentas aspecto de pudrición, desarrollándose sobre su superficie un moho grisáceo, haciéndola inservible para la vinificación. </p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p></p>
<p><strong> Cultural:</strong> empleo de variedades y portainjetos no vigorosos, resistentes a la Botrytis, aplicar tratamientos preventivos contra los Lobesia botrana, responsables de las heridas de las bayas. </p>
<p>No abusar de los abonos nitrogenados, la utilización de cobre aumenta el espesor del hollejo y por lo tanto su dureza, impidiendo la penetración del hongo, realizar podas equilibradas y podas en verde para favorecer la aireación de la cepa, restringir riegos, retirar y destruir racimos afectados.</p>
<p>Estaciones de alerta automáticas en viñedos particulares.</p>
<p></p>
<p><strong> Biológico:</strong> el empleo del hongo <i>Trichoderma harzianum, Trichoderma viride y Trichoderma richodex</i> como agentes competidores, aunque con una eficacia insuficiente. <i>Aureobasidium pullulans</i> antagonista del hongo <i>Botrytis cinerea</i>, compitiendo con el mismo por el espacio y nutrientes. <i>Bacillus subtilis</i> presenta un modo acción múltiple, detiene la germinación de las esporas de los fitopátogenos e inhibe su establecimiento en la superficie de la planta. También mediante microorganismos parásitos como <i>Gliocladium roseum</i>. </p>
<p></p>
