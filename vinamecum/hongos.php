<h1 id="hongo4">EXCORIOSIS (<i>Phomopsis viticola</i>)</h1>
<p>Clase Eumicetos. Subclase Ascomicetos. Familia <i>Caliciacae</i></p>
<p></p>
<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p><i>Phomopsis viticola Sacc.</i></p>
<p>Esta enfermedad se manifiesta en casi todos los viñedos españoles, principalmente en zonas del Norte y zonas lluviosas.</p>
<p></p>
<p style="font-size:20px;">•	Ciclo biológico</p>
<p>El ciclo se inicia a partir de picnidios desarrollados en la madera necrosada y del micelio blanquecino de los sarmientos y contenido en las yemas durante el otoño anterior.</p>
<p>Las esporas están contenidas dentro de los picnidios, que maduran durante todo el invierno hasta el inicio de la brotación, en primavera, y con la acción de la lluvia y los vientos las esporas se esparcen infectando el entorno. Después de un periodo de incubación de 2 semanas aproximadamente, el micelio se desarrolla en la base de los pámpanos, avanzando con el crecimiento de los mismos, hasta que se produzca la contaminación.</p>
<p>Durante el verano el hongo continúa su evolución y contaminación a nuevas yemas, pero si el verano es seco se queda inactivo, aunque en otoño cuando la temperatura desciende reanuda su actividad, formándose los picnidios en los sarmientos cerrando el ciclo anual.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>La enfermedad puede afectar a todos los órganos de la vid. Los síntomas más claros se observa en los órganos jóvenes de las plantas, pudiendo ser diferentes según la etapa de desarrollo.</p>
<p>En los brotes jóvenes: las lesiones (necrosis) aparecen en los primeros entrenudos de la base se los pámpanos, de forma alargada y color oscuro, que se van volviendo negros con reflejos azulados. También pueden aparecer las lesiones en pedúnculos de las inflorescencias y/o peciolos. Durante el crecimiento de los sarmientos las grietas corticales cicatrizan y se vuelven ásperas. </p>
<p>En el agostamiento la evolución de la necrosis se detiene, volviéndose la corteza de color blanquecina, con numerosos puntos negros (picnidios). </p>
<p>En invierno aparecen puntos negros y manchas oscuras irregulares con el centro blanco, por toda la superficie de los pámpanos infectada. Si se ha producido mucha contaminación, la epidermis se levanta, dejando pasar el aire bajo ella, lo que produce un aspecto al sarmiento de brillo blanquecino-plateado.</p>
<p>Los brotes se rompen debido a los estrangulamientos, la acción de viento y el peso de los racimos; además las yemas atacadas no brotarán al siguiente año. </p>
<p>Hojas: hay afección en los peciolos, pedúnculos y nervios principales, produciéndose manchas oscuras que pueden arrugar y doblar la hoja hacia abajo y producir pequeños orificios que provocan el marchitamiento, deformación, desecación y caída de la hoja. </p>
<p>Bayas: se marchitan gradualmente.</p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p></p>
<p><strong> Cultural:</strong> empleo de material vegetal sano y certificado. Prevenir condiciones que puedan causar estrés a las plantas durante al menos los 4 primeros años de crecimiento, favorecer el sistema radicular y no tanto el aéreo, evitar podas defectuosas con grandes heridas y rebajes, podas verticales, evitar podas tempranas, proteger las heridas de poda con “masilla o protector”, eliminar los restos de poda con infección, desinfectar los útiles de poda. </p>
<p>Evitar encharcamientos y compactación del terreno. </p>
<p></p>
<p><strong> Químico:</strong> el azufre espolvoreado es bastante eficaz. Para el control de la enfermedad, pueden emplearse láminas de vidrio con vaselina a la altura de los pulgares para construir la curva de esporulación. </p>
