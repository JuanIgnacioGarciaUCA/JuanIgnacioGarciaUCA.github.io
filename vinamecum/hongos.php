<h1 id="hongo6">OIDIO (<i>Erysiphe necator</i>)</h1>
<p>Subclase Ascomicetos. Familia <i>Erisifacae</i></p>
<p></p>
<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p><i>Erysiphe necator, Schwein</i>.</p>
<p>Se trata de una enfermedad muy extendida por toda España, perjudicando más a variedades sensibles.</p>
<p></p>
<p style="font-size:20px;">•	Ciclo biológico</p>
<p>El hongo pasa el invierno como micelio el interior de las yemas, o en forma de peritecas en las hojas, sarmientos y en la corteza de la cepa. En primavera cuando las temperaturas superan los 15ºC, el micelio se desarrolla sobre cualquier órgano de la vid, provocando la muerte de las células vegetales formando manchas pardas.</p>
<p>Si el origen de la infección es a partir de las peritecas invernantes, al madurar producen esporas que germinan y producen un micelio con las mismas características.</p>
<p>Cuando el micelio se desarrolla se forman los conidióforos, mediante la acción de viento son trasportados sobre cualquier órgano de la vid, constituyendo el polvo blanquecino ceniciento que da nombre a la enfermedad formando la contaminación primaria.</p>
<p>Si las condiciones climáticas son favorables, las conidias germinan dando lugar al micelio, que se desarrolla y produce contaminaciones secundarias durante el ciclo vegetativo.</p>
<p>En otoño se forman las peritecas a partir de los micelios, formadas por sacos de esporas que germinarán en la primavera siguiente.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>Hojas: tanto en el haz como en el envés, aparece un polvillo blanco ceniciento, debajo del cual se puede apreciar puntos oscuros de necrosis en el limbo. Si la infección es intensa las hojas se crispan y los bordes se abarquillan hacia el haz. </p>
<p>En los pámpanos y sarmientos aparecen manchas de color verde oscuro, que se agrandan y oscurecen hasta ennegrecer. </p>
<p>En los racimos el ataque es muy grave porque el hongo detiene el crecimiento del hollejo, y como el grano continua su desarrollo, las bayas se agrietan y se rajan. Además se vuelven de color plomizo, recubriéndose de polvillo ceniciento que si se quita pueden verse puntitos de necrosis. </p>
<p>El oídio reduce la fotosíntesis y altera la composición de azúcar, proteínas, taninos y polifenoles en las bayas. Los vinos producidos con uvas atacadas por oídio son generalmente de menor calidad, debido a la falta de madurez de la uva, alta acidez y falta de color (tintos). Además el hongo produce compuestos volátiles con olores desagradables, pudiendo atenuarse después de la vinificación.</p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p></p>
<p><strong> Cultural:</strong> se recomienda podar en verde para ventilar y airear la planta, y que los productos fitosanitarios tengan mejor difusión y alcance.</p>
<p>Eliminar restos de poda, emplear de sistemas de conducción que permitan buena circulación de aire a través de la cepa e impida el sombreado es también muy beneficioso, disminuir o suprimir abonos nitrogenados.</p>
<p></p>
<p><strong> Químico:</strong> sólo la protección química preventiva permite controlar el desarrollo del oídio de manera efectiva, el azufre presenta buena eficacia, siempre que los ataque no sean muy severos Se recomienda emplear productos químicos autorizados de forma específica en el Registro de productos fitosanitarios del MAPAMA: </p>
<p></p>
<p><a href="http://www.mapa.gob.es/es/agricultura/temas/sanidad-vegetal/productos-fitosanitarios/registro/productos/forexi.asp?e=0&plagEfecto=202">http://www.mapa.gob.es/es/agricultura/temas/sanidad-vegetal/productos-fitosanitarios/registro/productos/forexi.asp?e=0&plagEfecto=202</p>
