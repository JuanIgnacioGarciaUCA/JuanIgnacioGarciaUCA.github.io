<h1 id="Reptiles">REPTILES</h1>
<p>Las serpientes y los lagartos son muy importantes para mantener el equilibrio natural en el viñedo, ya que se alimentan de insectos y roedores dañinos. </p>
<p><strong> Culebra bastarda (<i>Malpolon monspessulanus</i>)</strong></p>
<p>Son de color gris, pardo rojizo, oliváceo, verdoso o casi negro, con la cabeza grande, cejas altas y salientes, iris de los ojos anaranjado y cuerpo escamado. A pesar de que sea el reptil más grande la Península Ibérica (hasta 2 m de longitud total) es inofensiva para el hombre siempre que no se encuentre acosada, ya que debido a la situación posterior de sus colmillos, es complicado que sean efectivos al morder al humano. Su veneno es perjudicial para distintos roedores, conejos y aves. También se alimenta de saltamontes y langostas.</p>
<p>Necesita refugios como muretes de piedras o vegetación.</p>
<p></p>
<li> <img class="w3-image" src="imagenes control biologico/reptiles/REPTILES.Culebrabastarda(Malpolonmonspessulanus).DavidHerreroGonzalez.jpg"></a></li>
<p></p>
<p><strong> Culebra de escalera (<i>Elaphe scalaris</i>)</strong></p>
<p>Miden hasta 1,5 m de longitud, con escamas lisas, de color gris amarillento o pardo claro. Es una especie inofensiva para el hombre, no tienen veneno.</p>
<p>Esta especie es un gran depredador de roedores, también de pájaros y reptiles. Busca refugio en la vegetación, árboles y cerca de fuentes de agua como arroyos. </p>
<p></p>
<li> <img class="w3-image" src="imagenes control biologico/reptiles/REPTILES.Culebradeescalera(Elaphescalaris).MiguelAngelLinares.jpg"></a></li>
<p></p>
<p><strong> Lagarto verde (Lacerta viridis)</strong></p>
<p>Miden hasta 13 cm de longitud, cuerpo robusto, el macho es verde con motas negras, y la hembra es verde-parda con manchas o líneas, ambos con el abdomen amarillo y garganta azulada. </p>
<p>Se alimentan de orugas del suelo, saltamontes, insectos, arañas, etc.</p>
<p></p>
<li> <img class="w3-image" src="imagenes control biologico/reptiles/REPTILES.Lagartoverde(Lacertaviridis).PedroLunaGuillen.jpg"></a></li>
<p></p>
<p><strong> Lagarto ocelado (Lacerta lepida)</strong></p>
<p>Es el más grande (hasta 0,5 m de longitud) y aplanado de los lagartos verdes, de coloración verdosa con manchas negras en el dorso y manchas azules en los flancos.</p>
<p>Se alimenta principalmente de roedores y lagartijas, además de insectos y topillos atacando las madrigueras.</p>
<p>Busca refugio en rocas, troncos caídos y madrigueras de otros animales.</p>
