<p style="font-size:20px;">•	Neurópteros</p>
<p>Nombres vulgares: hormigas león, crisopas.</p>
<p>La familia de las<i> Chrysopidae</i>, delicado insecto de color verdoso, por lo general de hábitos crepusculares o nocturnos, de patas marchadoras, antenas largas, alas grandes y raticuladas. Las larvas son terrestres y grandes depredadoras de pulgones y cochinillas siendo de color verde. </p>
<p>Para favorecer su presencia pueden colocarse refugios-nido especiales para crisopas, ya que los requieren para pasar el invierno, colocados a 2 m de altura y protegidos del viento.</p>
<p>El cultivo en masas de estos insectos, se emplea como control biológico de pulgones, así como del mosquito verde, trips, araña amarilla, acariosis o erinosis de la vid.</p>
