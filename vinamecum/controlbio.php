<p style="font-size:20px;">•	Aves Rapaces </p>
<p></p>
<p></p><p><strong> Cernícalo vulgar (<i>Falco tinnunculus</i>)</strong></p>
<p>Miden hasta 35 cm de longitud, los machos son de color gris, tostado y pardo-rojizo con un moteado negro, y las hembras pardo-rojizas con moteado negro. Se cierne moviendo rápidamente los extremos de las alas y manteniendo la cola extendida. Son aves sedentarias. Para favorecer su presencia colocar cajas-nido, con medidas de (25 Alt. x 25 Anch. X 40 Prof.) y orificio de entrada de 10 cm, a una altura de 18 cm.</p>
<p>Se alimenta de pequeños mamíferos y de insectos. </p>
<p></p>
<li> <img class="w3-image" src="imagenes control biologico/Aves rapaces/1.jpg"></a></li>
<p></p>
<p></p><p><strong> Ratonero común (<i>Buteo buteo</i>)</strong></p>
<p>Miden hasta 56 cm de longitud, con las alas anchas y colorido variable, pudiendo ser muy pálido, oscuro o rojizo. Son sedentarios, aunque es común verlo en postes de luz vigilando sus presas. Para integrarlo en el viñedo es conveniente colocar posaderos. Para favorecer su presencia hay que colocar cajas-nido a más de 4 m de altura (1 caja-nido cada 5-10 hectáreas).</p>
<p>Se alimenta de micromaníferos como roedores y conejos, e invertebrados.</p>
<p></p>
<li> <img class="w3-image" src="imagenes control biologico/Aves rapaces/2.jpg"></a></li>
<p></p>
<p></p><p><strong> Búho chico (=<i>Austillo) (Asio otus)</i></strong></p>
<p>Miden hasta 20 cm, es principalmente nocturno, de color pardo moteado, presenta “orejas”. Es un ave sedentaria o visitante estival. Se alimenta de grandes insectos y mamíferos. </p>
<p>Para favorecer su presencia pueden colocarse cestas nido de mimbre de 0,5 m de diámetro a 5 – 6 m de altura en los árboles, ya que no construyen su propio nido (Garcés et al., 2015).</p>
<p></p>
<li> <img class="w3-image" src="imagenes control biologico/Aves rapaces/3.png"></a></li>
<p></p>
<p><strong> Mochuelo común (<i>Athene noctua</i>)</strong></p>
<p>Miden hasta 22 cm, son muy pequeños, particularmente diurnos, de color pardo con moteados blancos y con grandes ojos amarillos. Hace un movimiento muy característico con la cabeza inclinándola, a veces dándole un giro de 180 º. Es un ave sedentaria. </p>
<p>Colocar cajas nido si por la zona del viñedo no hubiese troncos huecos donde suele anidar, con medidas de (50 Alt. x 20 Anch. X 20 Prof.) y orificio de entrada de 8 cm, a una altura de 36 cm. (1 caja-nido cada 5-10 hectáreas).</p>
<p>Se alimenta de pequeños roedores, pájaros e insectos.</p>
<p></p>
<li> <img class="w3-image" src="imagenes control biologico/Aves rapaces/4.png"></a></li>
<p></p>
<p><strong> Lechuza común (<i>Tyto alba</i>)</strong></p>
<p>Miden hasta 35 cm de longitud, tienen una característica forma de disco facial, en forma de corazón y de color blanca en el que destacan los ojos oscuros. El cuerpo es de tono dorado, con el pecho y vientre blanco. Son aves nocturnas aunque también pueden cazar de día. </p>
<p>Colocar cajas nido a gran altura para favorecer su presencia (1 caja-nido cada 5-10 hectáreas), con medidas de (60 Alt. x 26 Anch. X 40 Prof.) y orificio de entrada rectangular de (14x15) cm, a una altura de 36 cm. (1 caja-nido cada 5-10 hectáreas).</p>
<p>Se alimenta principalmente de roedores y reptiles.</p>
<p></p>
<li> <img class="w3-image" src="imagenes control biologico/Aves rapaces/5.png"></a></li>
<p></p>
<h1 id="Mamiferos">MAMÍFEROS</h1>
<p></p>
<p><strong> Erizo común (<i>Erinaceus europeaus</i>)</strong></p>
<p>Miden hasta 30 cm de longitud. Con el cuerpo rechoncho y hocico apuntado. Tienen un pelaje con mezcla de pelos y de numerosas espinas. Tienen la capacidad de enrollarse formando una bola para protegerse. Son de color pardo con el vientre blanquecino. Generalmente viven en densas coberturas vegetales, por lo que la vegetación espontánea en el viñedo, así como aglomeraciones de rocas o árboles favorece su presencia, también pueden colocarse refugios especiales para erizos en lugares privados, ya que son muy asustadizos. Es de hábitos nocturnos, e invernan durante las estaciones frías. </p>
<p>Se alimenta de caracoles, lombrices y babosas, así como frutos. </p>
