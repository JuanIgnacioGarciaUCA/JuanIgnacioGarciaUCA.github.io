<h1 id="Aracnidos">ARÁCNIDOS</h1>
<p style="font-size:20px;">•	Arañas</p>
<p>Las arañas son depredadoras con gran capacidad de reducir poblaciones de insectos plaga, tanto al alimentarse de larvas de lepidópteros y orugas, como capturando mosquito verde y otros insectos con las telarañas que tejen. Suelen ocupar una posición terminal en las cadenas trópicas, ya que son grandes depredadoras (Vázquez et al., 2008).</p>
<p>Las arañas atacan a sus presas en relación a la tasa de encuentro, por ello cuanto mayor es la densidad de una población presa, mayor proporción de ataques supondrá. Son muy sensibles a los tratamientos químicos.</p>
<p><i>M. pallidus, O. salticus y Araneus sp.</i> presentan preferencia positiva frente a las larvas de lepidópteros defoliadores. Según Nyffeler & Sunderland (2003) la dieta de las arañas en agroecosistemas incluye Díptera, Homóptera, Lepidóptera, Heteróptera, Himenóptera y Coleóptera, que comprenden el 95% del total de presas consumidas. </p>
