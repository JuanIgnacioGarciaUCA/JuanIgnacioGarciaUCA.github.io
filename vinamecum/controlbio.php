<p>Pertenecen a la clase de los Arácnidos, están estrechamente emparentados con las arañas y ácaros. Se alimentan de insectos, arañas y roedores. Prefieren ambientes húmedos, se refugian en grietas, rocas, cortezas de árboles, etc., por ello es conveniente la construcción tradicional de pequeños muretes de rocas o refugios de insectos.</p>
