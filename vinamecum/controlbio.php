<p style="font-size:20px;">•	Dípteros</p>
<p>Nombres vulgares: moscas, mosquitos, tábanos, típulas, moscardas.</p>
<p>Hay una gran variedad de Dípteros. Tanto en estado larvario como adulto juegan un papel importante en el control de plagas. Algunas larvas terrestres son depredadoras de varios insectos. La mayoría son diurnos y desarrollan su actividad en días soleados, aunque algunos pueden ser nocturnos o crepusculares.</p>
<p><i>Syphidae (Epysirphus balteatus)</i>: denominadas “moscas de las flores”, ya que los adultos se alimentan del néctar. Su cuerpo se parece al de las avispas, y pueden quedarse suspendidas en vuelo. Las larvas se alimentan de los pulgones.</p>
<p><i>Cecidomyiidae</i> (<i>Aphidoletes aphidimiza, Feltiella acarisuga</i>): los adultos son mosquitos de patas largas y frágiles, las larvas son depredadoras de pulgón y de araña roja.</p>
<p><i>Muscidae</i> (<i>Coenosia attenuatay</i>): conocidas comúnmente por “mosca tigre”, se parece a la mosca doméstica pero son algo más pequeñas, el adulto se alimenta de la mosca blanca.</p>
<p>Además todos los adultos se alimentan de polen y néctar, por lo que la presencia de flores es necesaria para su existencia.</p>
