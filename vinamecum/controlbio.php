<h1 id="Insectos">INSECTOS</h1>
<p>Son uno de los organismos más diversos del planeta (Fröhle et al., 2017). Algunos juegan un papel vital en el correcto funcionamiento de los ecosistemas, gracias a su comportamiento como parasitoides, agentes entomopatógenos y depredadores, limitando el desarrollo de plagas del viñedo. Además muchas especies son polinizadoras produciendo un impacto sobre el cultivo y la proliferación de plantas beneficiosas. También hay especies capaces de acelerar la descomposición de la materia orgánica, desmenuzando excrementos, plantas, animales muertos, etc., con ello mejorando la fertilidad del suelo.</p>
<p></p>
<p style="font-size:20px;">•	Coleópteros</p>
<p>Nombres vulgares: escarabajos, ciervos voladores, mariquitas, barrenillos, carcomas y gorgojos.</p>
<p>Una de las especies más comunes son la <i>Coccinella septempunctata</i>, comúnmente llamada mariquita de siete puntos, y la <i>Adalia bipunctata</i> (mariquita de dos puntos), siendo los adultos de cuerpo globoso, de color rojo sobre el que resaltan manchas negras y con seis patas.</p>
<p>Son grandes depredadores en nuestros viñedos, ya que se alimentas tanto de las etapas larvarias como adultas de la polilla del racimo, piral, pulgón, ácaros, melazo, etc. </p>
<p>Plantas que pueden ser un buen refugio pueden ser las adelfas, hinojo, madroño, leguminosas, etc. Para facilitar su presencia pueden colocarse refugios-nidos especiales para mariquitas, colocados en ramas o el suelo.</p>
<p>Otros Coleópteros son los <i>Estafilinos</i>, escarabajos alargados terrestres, depredadores de insectos como la pupa de la polilla del racimo, ácaros.</p>
<p>Los <i>Carábidos</i> son escarabajos de diversos colores y tamaños, alargados, depredadores de la polilla del racimo y gusanos grises.</p>

