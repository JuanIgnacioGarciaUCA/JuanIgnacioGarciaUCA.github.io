<h1>AVES</h1>
<p id="Avesinsectivoras"></p>
<p style="font-size:20px;">•	Aves Insectívoras </p>
<p>Se caracterizan por su pequeño tamaño y gran actividad metabólica. Durante el periodo de nidificación se alimentan de una gran cantidad de insectos, principalmente de larvas y adultos.</p>
<p>Las principales especies que se instalan en las cajas-nido son los carboneros y herrerillos, así como colirrojos, pampamoscas, chochines, etc. Según el diseño de la caja y el tamaño de la apertura de entrada del pájaro, es posible potenciar la especie deseada, pudiendo de esta manera dirigir el control biológico de la plaga que queramos controlar. Las anidaderas deben colocarse en otoño, en árboles y cerca de puntos de agua, orientadas al Este y proteger su orificio de entrada del viento dominante. Deben tener un diámetro máximo de 36 mm, colocarse entre (3 – 5) m de altura y alejadas del tronco del para evitar la entrada de depredadores de huevos, polluelos o los pájaros (Izquierdo Rosique & Izquierdo Rosique, 2007).</p>
<p></p>
<p><strong> Carbonero común (<i>Parus major</i>)</strong></p>
<p>Miden unos 14 cm, de color amarillo con una banda negra a lo largo del pecho y la cabeza negra con mejillas blancas. Para favorecer su presencia colocar cajas-nido, con medidas de (25 Alt. x 15 Anch. X 15 Prof.) cm y orificio de entrada de 3,5 cm, a una altura de 20 cm.</p>
<p>Se alimenta de insectos y semillas.</p>
<p></p>
<li> <img class="w3-image" src="imagenes control biologico/insectivoras/1.jpg"></a></li>
<p></p>
<p><strong> Herrerillo común (<i>Parus caeruleus</i>)</strong></p>
<p>Miden hasta 11,5 cm de longitud, son de color azul cobalto, mejillas blancas y zona inferior amarilla, con una vistosa raya central negra. Para favorecer su presencia colocar cajas-nido, con medidas de (25 Alt. x 12 Anch. X 12 Prof.) cm y orificio de entrada de 3 cm, a una altura de 18 cm.</p>
<p>Se alimenta de insectos y semillas.</p>
<p></p>
<li> <img class="w3-image" src="imagenes control biologico/insectivoras/2.jpg"></a></li>
<p></p>
<p><strong> Petirrojo europeo (<i>Erithacus rubecula</i>)</strong></p>
<p>Es un pequeño pájaro insectívoro, mide 20 cm de envergadura, el color del pecho y la cara es anaranjado, cuello azulado y vientre blanquecino. Es diurno y sociable. Se alimenta de invertebrados terrestres como insectos, arañas, gusanos, y también frutas y bayas. </p>
<p>Para favorecer su presencia colocar cajas-nido, con medidas de (28 Alt. x 16 Anch. X 16 Prof.) cm y orificio de entrada de 4,5 cm, a una altura de 20 cm.</p>
<p></p>
<li> <img class="w3-image" src="imagenes control biologico/insectivoras/3.jpg"></a></li>
<p></p>
<p><strong>Colirrojo tizón (<i>Phoenicurus ochruros</i>)</strong></p>
<p>Es un ave muy común, mide 14 cm de longitud, con el cuerpo de color negro y la cola rojiza-anaranjada. Para favorecer su presencia colocar cajas-nido, con medidas de (25 Alt. x 23 Anch. X 14 Prof.) cm y orificio de entrada de (10x3) cm, a una altura de 22 cm.</p>
<p>Es un gran insectívoro, tanto de larvas como de adultos que se encuentren en el viñedo, lanzándose sobre ellas.</p>
<p>Las aves rapaces son grandes depredadoras de roedores (Muñoz, 2011) aunque también se alimentan de reptiles, gusanos, saltamontes y escarabajos. Contribuyen como un poderoso componente en el control integrados de plagas (Khramtsov & Timchenko, 1976). </p>
