<p style="font-size:20px;">•	Hemípteros (=Heteróptero)</p>
<p>Nombres vulgares: chinches, chinches de jardín, garrapatillos, sampedritos, barqueros, zapateros, escorpiones de agua.</p>
<p>La mayoría son terrestres, los <i>Pentatomidae</i> suelen ser hexagonales o alargados, algunas especies son depredadores, como los<i> Orius Spp.</i> (chinche de las flores) tanto las larvas como los adultos se alimentan de trips, ácaros, larvas y huevos de polillas, aunque también se alimentan de polen.<i> Zicrina coerulea</i> (chinche azul), de un color metalizado es un gran depredador de insectos como la altica o coquillo.</p>
