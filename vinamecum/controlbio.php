<p style="font-size:20px;">•	Ácaros </p>
<p>La gran familia de los ácaros Phytoseiidae tiene un gran interés en el control biológico e integrado, ya que son depredadores de especies de ácaros plaga y algunos insectos. Otra característica es que son omnívoros, se alimentan también de polen, melazas, hongos, etc., pudiendo así sobrevivir. </p>
<p>Las especies más comunes y de las que se dispone de manera comercial son:<i> Amblyseius andersoni</i> y<i> Phytoseiulus persimilis</i> depredadores de arañas rojas, así como trips y otros insectos;<i> Euseius stipulatus</i> se alimenta principalmente de la cochinilla algodonosa;<i> Neoseiulus barkeri</i> se alimenta de larvas de Tisanópteros y ácaros principalmente.</p>
<p>Son muy sensibles a los tratamientos químicos.                              </p>
