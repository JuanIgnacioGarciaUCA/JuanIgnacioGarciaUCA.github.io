<p></p>
<p><strong> Murciélago común (Pipistrellus pipistrellus)</strong></p>
<p>Miden hasta 4,5 cm de longitud, son de color pardo, con el hocico corto y redondeado, de orejas conspicuas. Viven en lugares resguardados.</p>
<p></p>
<li> <img class="w3-image" src="imagenes control biologico/mamiferos/2.jpg"></a></li>
<p></p>
<p><strong> Murciélago ratero (<i>Moyotis moyotis</i>)</strong></p>
<p>Miden hasta 8 cm de longitud, son de color gris parduzco, con el hocico bastante afilado, orejas grandes y membranosas. Viven en lugares resguardados.</p>
<p>Los murciélagos son grandes depredadores de insectos, además al ser de hábitos crepusculares y nocturnos al igual que una gran cantidad de plagas de la vid, los convierte en grandes aliados para los viñedos, alimentándose de polilla de la vid, piral, gusano gris, etc.</p>
<p>Para favorecer lapresencia de los muerciélagos colocar refugios especiales de murciélago, tintados de color negro, instalados en zonas despejadas en algún árbol o poste dirección Sudeste y en alto (5 m de altura). </p>
