<p style="font-size:20px;">•	Himenóptero</p>
<p>Nombres vulgares: avispas, abejas, hormigas, etc.</p>
<p>Los <i>Terabrantia</i> en estado larva son parásitos de otros insectos, de ahí que se incluyan dentro de la división Parasítica. </p>
<p>Este gran grupo todos ellos parasitoides como son las avispas, abejas y hormigas, tienen la capacidad de depositar sus huevos en el interior de las larvas de otros insectos, como de polillas, devorando las larvas neonatas a su huésped. Por ello son de un control muy efectivo en el viñedo. Además las avispas pueden atacar orugas. </p>
<p>Se ven favorecidas por las construcciones tradicionales y las flores, además podemos colocar refugios-nido especiales para Himenópteros, suelen ser de madera o cemento.</p>
<p><i>Elachertus affinis y Campoplex capitator</i> son el principal parasitoide la polilla de la vid,<i> Anagyrus pseudococci</i> es un importante parasitoide de la cochinilla de la vid y el melazo.</p>
