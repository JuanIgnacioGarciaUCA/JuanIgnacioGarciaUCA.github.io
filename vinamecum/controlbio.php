<p style="font-size:20px;">•	Dermáptera</p>
<p>Nombres vulgares: tijeretas</p>
<p>De cuerpos alargados, de color oscuro, con piezas bucales masticadoras, generalmente alados, en el abdomen tienen dos cercos en forma de pinza.</p>
<p>Aunque viven en la tierra, suben a la vid para alimentarse de larvas de polillas o pulgones, convirtiéndolos en buenos depredadores en el viñedo. La materia orgánica estimula su presencia, así como la colocación nidos especiales para tijeretas, colocados en ramas o el suelo.  </p>
