<h1 id="acaro3">ARAÑA AMARILLA COMÚN (<i>Tetranychus urticae</i> Koch)</h1>
<p>Familia <i>Tetranquidae</i></p>
<p style="font-size:20px;">•	Morfología</p>
<p>Huevos: miden 0,1 mm de diámetro, inicialmente incoloro en la puesta, volviéndose amarillo a ámbar al acercarse a la eclosión, de forma esférica.</p>
<p>Larvas: semejantes al adulto, pero de menor tamaño y hexápoda.</p>
<p>Adultos: miden 0,55 mm por término medio, siendo algo mayores las hembras. El cuerpo es transparente, percibiéndose en ocasiones dos manchas oscuras en los laterales, causadas por los alimentos visibles a través de la epidermis. Su color varía en función de la planta huésped, la edad de ácaro, la época del año, etc. Cambiando tonalidades de amarillo a verde, con forma oval y cuatro pares de patas. </p>
<p style="font-size:20px;">•	Ciclo anual</p>
<p>Las hembras, fertilizadas, pasan el invierno refugiadas en la corteza de árboles, hojarasca, hierbas adventicias o el suelo. En primavera se trasladan preferentemente a cultivos herbáceos para alimentarse, situándose en el envés de las hojas. La puesta de huevos la realizan sobre las hojas, pasando por los estados: larva hexápoda (5 días), ninfas (10 días), y adulto, completándose una generación en tan solo 15 días. En el mes de junio ascienden hasta las hojas de la vid para alimentarse, para más tarde colonizar los nietos. A finales de otoño las hembras van en busca de refugio para invernar.</p>
<p>Hasta 12 generaciones anuales.<p>
<p style="font-size:20px;">•	Daños</p>
<p>Los principales daños los produce en las hojas, aparecen moteadas, con la presencia de puntos amarillentos y necróticos, respetando los nervios principales. Posteriormente la hoja amarillea y se cae si el ataque es severo. Además pueden atacar pámpanos, raspones y bayas. Los síntomas de los racimos pueden confundirse con el oídio debido a las mudas blanquecinas del ácaro, ya que esta especie produce abundantes sedas en las partes atacadas, pudiendo cubrir totalmente la planta si el ataque es fuerte.</p>
<p>Los síntomas suelen manifestarse primero en las lindes del viñedo más cercanas a las hierbas adventicias.</p>
<p style="font-size:20px;">•	Establecimiento de umbral de referencia</p>
<p>Observar 200 cepas por parcela clasificándolas en tres clases: (0) si no hay ataque, (I) si el ataque se produce en las hojas inferiores y hasta los racimos, (II) cuando el ataque cubre toda la masa foliar de la cepa. Realizar el muestreo en floración, bayas tamaño guisante y envero.</p>
<p>Tratar en función del % de plantas ocupadas por la plaga, nivel de defoliación, estado de madurez del racimo y de la concentración de azúcares, pudiendo hacerse tratamientos en rodales.</p>
<p style="font-size:20px;">•	Métodos de control</p>
<p><strong>Cultural:</strong> eliminar restos de poda, las hierbas espontáneas del viñedo y sus alrededores, siendo la época del “lloro” el mejor momento. No abusar de abonos nitrogenados. </p>
<p><strong>Biológico:</strong> empleando depredadores como los ácaros: <i> Neoseiulus californicus, kampinodromus aberrans, Thyphlodromus pyri, Typhlodromus phialatus y Amblyseius swirskii</i> ; además de otros insectos depredadores como: Coccinélidos (<i> Stethorus punctillum Eiese</i> ), Crisópidos (<i> Chrysoperla carnea</i>  Scheneid) y Antocóridos (<i> Orius spp.</i> ).</p>
<p><strong>Químico:</strong> el azufre espolvoreado da buenos resultados, se recomienda emplear productos químicos autorizados de forma específica en el Registro de productos fitosanitarios del MAPAMA: </p>
<p><a target="_blank" href="http://www.mapa.gob.es/es/agricultura/temas/sanidad-vegetal/productos-fitosanitarios/registro/productos/forexi.asp?e=0&plagEfecto=854">http://www.mapa.gob.es/es/agricultura/temas/sanidad-vegetal/productos-fitosanitarios/registro/productos/forexi.asp?e=0&plagEfecto=854</a></p>

