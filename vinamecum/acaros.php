<h1 id="acaro6">ERINOSIS DE LA VID (<i>Colmerus vitis</i> Pagenstecher)</h1>
<p>Familia <i>Eriofidae</i></p>

<p style="font-size:20px;">•	Morfología</p>
<p>Huevos: miden 30 – 35 µm, de color blancos y ovalados.</p>
<p>Adultos: miden 0,2 mm de longitud, de color blanco amarillo claro, con estrías transversales en el abdomen, y pequeños tubérculos de forma elíptica, y cuatro pares de patas.</p>
<p style="font-size:20px;">•	Ciclo anual</p>
<p>Existen tres tipos de razas según se sitúen:</p>
<p>En las hojas: Raza que produce falsas agallas. Invernan en estado adulto, en grupos, protegidos bajo la corteza del tronco de la vid. En primavera ascienden a las hojas recién formadas, provocando la formación de falsas agallas, donde las hembras depositan en el interior los huevos, viviendo dentro de las mismas con su descendencia hasta que necrosan la hoja y se mudan a una nueva. Tienen hasta 7 generaciones anuales. En otoño regresan a la corteza a invernar.</p>
<p>En las yemas: es la más perjudicial, invernan en estado adulto en las yemas basales principalmente. En primavera comienzan su actividad, alimentándose del brote formado, hasta 12 generaciones anuales.</p>
<p>Raza que curva la hoja: Ciclo desconocido.</p>
<p style="font-size:20px;">•	Daños</p>
<p>Cada raza produce daños diferentes:</p>
<p>Razas de las hojas: Produce falsas agallas “cecidios”, abultadas por el haz y abiertas por el envés, con el interior tapizado por una abundante pilosidad blanquecina al principio y parduzca rojiza más tarde. Esta característica pilosidad puede aparecer en los peciolos de las hojas, yemas, zarcillos, brotes o inflorescencias.</p>
<p>Razas localizadas en las yemas: Pueden impedir la brotación de la yema, apareciendo una borra hinchada de color marrón rojiza. Cuando la yema brota, lo hace con retraso, con hojas y racimos deformados o inexistentes. </p>
<p>Razas que curvan las hojas: Cuando la hoja se desarrolla, adquiere un aspecto de abarquillamiento desde los limbos hacia el envés de la hoja. </p>
<p style="font-size:20px;">•	Establecimiento de umbral de referencia</p>
<p>Es difícil el seguimiento de la biología del ácaro, así como determinar la intensidad de la plaga. Debe intervenirse cuando el umbral sea más del 5 % de hojas con presencia de síntomas, para el caso de la raza de las yemas.</p>
<p style="font-size:20px;">•	Métodos de control</p>
<p><strong>Cultural:</strong> eliminar restos de poda.</p>
<p><strong>Biológico:</strong> empleo de ácaros fitoseidos, principalmente: <i> Typhlodromus phialatus, Amblyseius spp.</i>, e insectos: <i> Aeolothrips spp., Chrysoperla carnea y Orius spp.</i> </p>
