<h1 id="acaro5">ARAÑA ROJA (<i>Panonychus ulmi</i> Koch)</h1>
<p>Familia <i>Tetanquidae</i></p>

<p style="font-size:20px;">•	Morfología</p>
<p>Huevos: son diferentes en verano y en invierno. En verano miden 0,1 mm de diámetro, esféricos, ligeramente achatados y de color naranja. En invierno son más grandes, de color rojo intenso debido a las capas de cera protectora, con forma aplastada por arriba y con un pelo blanco insertado.</p>
<p>Larva: miden 0,2 mm y tienen 3 pares de patas, diferenciándolas de las ninfas con 4 pares de patas.</p>
<p>Adultos: la hembra mide 0,7 mm de longitud, fácilmente reconocible por su dorso muy abultado, sobre el que lleva 7 series transversales de sedas insertas en protuberancias blancuzcas, de color rojo o pardo rojizo. El macho mide 0,5 mm, color rojo más pálido, con el cuerpo estrecho hacia el extremo posterior. Ambos tienen cuatro pares de patas.</p>
<p style="font-size:20px;">•	Ciclo anual</p>
<p>Inverna en estado de huevo en las yemas, cicatrices de la poda, y debajo de la corteza del tronco. En casos de fuertes ataques con puestas abundantes, la madera adquiere una coloración rojiza. La eclosión de los huevos tiene lugar en primavera, y las larvas se sitúan preferentemente en el envés de las hojas para alimentarse, aunque luego ocupan ambas caras. Al final de primavera las hembras hacen sus primeras puestas en el envés de las hojas, hasta 7 generaciones anuales.</p>
<p style="font-size:20px;">•	Daños</p>
<p>Los principales daños los produce en las hojas, apareciendo picaduras por todo el limbo de las hojas rodeadas de una decoloración, pudiendo en ataques muy severos defoliar la cepa. Los ataques más perjudiciales son al inicio de la brotación. También pueden repercutir en la vegetación del año siguiente, por un mal agostamiento de los sarmientos y una deficiente alimentación de las yemas.</p>
<p style="font-size:20px;">•	Establecimiento de umbral de referencia</p>
<p>Tratar si al inicio de la brotación un 10 % de yemas tienen puestas de invierno.</p>
<p>Tratar si el 80 % de huevos de invierno eclosionan.</p> 
<p>Tratar al inicio de la puesta de invierno si la plaga en verano ha sido severa.</p>
<p style="font-size:20px;">•	Métodos de control</p>
<p><strong>Cultural: </strong>no abusar de abonos nitrogenados, quemar la madera de poda ya que puede albergar una parte importante de la puesta de huevos de invierno. </p>
<p><strong>Biológico:</strong> introducir depredadores como: <i> Amblyseius californicus, Amblyseius andersoni, Phytoseiulus persimilis, Feltiella acarisuga, Macrolophus pygmaeus, Neoseiulus cucumeris, Neoseiulus californicus, Neoseiulus cucumeris y Nesidiocoris tenuis</i> .</p>
