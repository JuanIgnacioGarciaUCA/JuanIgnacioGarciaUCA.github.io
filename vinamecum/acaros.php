<h1 id="acaro2">ÁCARO DE LA ROÑA (<i>Brevipalpus lewisi</i> McGregor)</h1>
<p>Familia <i>Tenuipapidae</i></p>

<p style="font-size:20px;">•	Morfología</p>
<p>Huevos: de color naranja o rojizo-naranja, ovales y alargados.</p>
<p>Larva y ninfa: color naranja-rojizo, con pelos dorsales gruesos.</p>
<p>Adulto: miden 0,3 mm de longitud, de color naranja-rojizo y dos manchas pequeñas y marrones, forma ovoide, cuerpo plano y ligeramente estrechado en la parte posterior, con cuatro pares de patas y movimientos lentos.</p>
<p><p style="font-size:20px;"><p>Las hembras adultas pasan el invierno bajo la corteza de los brazos de las cepas o entre las hojas secas de la vid sobre el suelo. En primavera recobran la actividad progresivamente, desde el estado “yema en punta verde” hasta “botones florales separados”, concentrándose masivamente en la base de los brotes jóvenes que adquieren un tono rojizo, cuando realizan la puesta (15/20 huevos).</p>
<p>Van colonizando la cepa progresivamente, desde los entrenudos de la base a las hojas, distribuyéndose por toda la planta durante el verano. En otoño los adultos buscan los refugios, para invernar. Hasta 4 generaciones anuales.</p>
<p style="font-size:20px;">•	Daños</p>
<p>En la vid pueden atacar cualquier órgano verde: base de los brotes, nudos, hojas, raspones e incluso las bayas. Los nudos, sobre todo los de la base, adquieren un tono oscuro al principio, luego se pardean y lignifican hasta resquebrajarse; las hojas presentan punteadas necróticas a lo largo de los nervios, muy visibles al trasluz; las partes afectadas de los raspones se tornan pardo oscuras en las zonas afectadas. En general se produce un debilitamiento de la cepa, con racimos pequeños, siendo la época de desborre la más peligrosa.</p>
<p style="font-size:20px;">•	Establecimiento de umbral de referencia</p>
<p>Realizar una prospección de síntomas en la base de los sarmientos y racimos una vez finalizado el ciclo vegetativo, y otra en la brotación observando los primeros entrenudos de la base de los pámpanos, aspecto roñoso en las uniones de los pedúnculos y hojas. Suberización en la epidermis de raspones y granos.</p>
<p style="font-size:20px;">•	Métodos de control</p>
<p><strong>Cultural: </strong>eliminar los restos de poda.</p>
<p><strong>Biológicos:</strong> se recomienda el empleo de ácaros depredadores: <i> Typhlodromus phialatus, Crysopa spp., y Stehorus punctillum</i> .</p>
<p><strong>Químicos:</strong> entre los productos que han dado buenos resultados están el azufre, sobre todo espolvoreado, además de acaricidas específicos, se recomienda emplear productos químicos autorizados de forma específica en el Registro de productos fitosanitarios del MAPAMA: </p>
<p><a target="_blank" href="http://www.mapa.gob.es/es/agricultura/temas/sanidad-vegetal/productos-fitosanitarios/registro/productos/forexi.asp?e=0&plagEfecto=859">http://www.mapa.gob.es/es/agricultura/temas/sanidad-vegetal/productos-fitosanitarios/registro/productos/forexi.asp?e=0&plagEfecto=859</a></p>

