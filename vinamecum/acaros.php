<h1 id="acaro1">ACARIOSIS (<i>Calepitrimerus vitis</i>  Nalepa)</h1>
<p>Ácaro eriófiro. Familia <i>Eriofidae</i></p>

<p style="font-size:20px;">•	Morfología</p>
<p>Huevos: miden 0,04 mm, redondos, blancos y quitinizados.</p>
<p>Adultos: miden 0,15 mm de longitud según estado, color amarillento, cuerpo alargado, abdomen anillado, con dos pares de patas en la parte anterior del cuerpo.</p>
<p style="font-size:20px;">•	Ciclo anual</p>
<p>Pasa el invierno como hembra adulta (deutogina) protegida bajo las escamas de las yemas o de las grietas de la madera de brazos y pulgares. En primavera recobran la actividad y efectúan la puesta en la zona basal de los brotes, primero, y más tarde en las hojas jóvenes. Prefieren alimentarse del envés. A principios de otoño aparecen las hembras invernantes y abandonan las hojas para invernar. Hasta 4 generaciones anuales.</p>
<p style="font-size:20px;">•	Daños</p>
<p>Los daños más graves tienen lugar en el desborre cuando coincide con las temperaturas bajas, ya que los ácaros se concentran en un brote, mientras que si las temperatura es más alta, la vegetación tiene un mayor desarrollo y el ácaro se dispersa por los distintos brotes.</p>
<p>Según el estado vegetativo del viñedo provocan diferentes daños:</p>
<p>Durante el inicio de la brotación se hace ésta lenta, ocasiona una brotación anormal, de hojas abarquilladas y abultadas, nervios muy patentes y sarmientos con entrenudos cortos. Si se mira una hoja a trasluz, se puede apreciar un moteado típico de puntos decolorados de tono amarillo debido a las picaduras de los ácaros. </p>
<p>En verano las hojas se tornan de color marrón con el envés blanquecino, pudiendo contener hasta 3000 ácaros/hoja. Provocando entrenudos cortos y enanismo, puede producirse corrimiento de las flores y frutos. </p>
<p style="font-size:20px;">•	Establecimiento de umbral de referencia</p>
<p>El umbral de tolerancia se sitúa en valores de 1 a 3 ácaros en yemas, y de 50 a 100 ácaros en hojas, si se superan estos umbrales se debe tratar.</p>
<p style="font-size:20px;">•	Métodos de control</p>
<p><strong>Cultural:</strong> realizar podas cortas (dos yemas), ya que la mayoría de ácaros invernan en las yemas centrales. Retirar y destruir los restos de poda de parcelas afectadas, el momento óptimo de realizar tratamientos es en el estado fenológico C. </p>
<p><strong>Biológicos:</strong> se recomienda el empleo de ácaros depredadores: <i> Typhlodromus phialatus, Typhlodromus pyri o Kampinodromus aberrans</i> , todos ellos ayudan con el control de la acariosis. </p>
<p><strong>Químicos:</strong> el azufre es muy efectivo, se recomienda emplear productos químicos autorizados de forma específica en el Registro de productos fitosanitarios del MAPAMA:</p>
