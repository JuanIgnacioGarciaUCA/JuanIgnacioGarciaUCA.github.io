<h1 id="acaro4">ARAÑA AMARILLA (<i>Eotetranychus carpini</i> Oud)</h1>
<p>Familia <i>Tetraniquidae</i></p>

<p style="font-size:20px;">•	Morfología</p>
<p>Huevos: miden 0,1 mm de diámetro, esféricos, lisos y translúcidos.</p>
<p>Estados intermedios: entre larva, ninfa, protoninfa y deutoninfa. Todos los estados con cuatro pares de patas, exceptuado el primero, la larva, que sólo tiene tres.</p>
<p>Adultos: la hembra mide 0,4 mm de longitud, de color amarillo, con puntitos negros a cada lado; la hembra invernante es de color amarillo limón, sin los puntos oscuros, pueden tejer hilos sedosos que les sirven de protección frente a los depredadores. El macho es más pequeño, alargado y piriforme. Ambos tienen cuatro pares de patas.</p>
<p style="font-size:20px;">•	Ciclo anual</p>
<p>Las hembras fecundadas se refugian debajo de la corteza del tronco durante el invierno. En primavera emigran a las partes verdes de la vid, al inicio de la brotación, perjudicando el desborre. Las hembras ponen los huevos (30 – 40 huevos/hembra) en el envés de las hojas a lo largo de los nervios, tras 8 – 10 días nacen las larvas, estado que dura dos semanas hasta convertirse en adultos.</p>
<p>Las primeras generaciones de araña amarilla centran su actividad en las hojas basales, dispersándose lentamente hacia el resto de la vegetación a medida que ésta se va secando. </p>
<p>Al finalizar el verano las hembras se refugian debajo de la corteza del tronco. Hasta 8 generaciones anuales.</p>
<p style="font-size:20px;">•	Daños</p>
<p>Los principales daños los ocasiona en las hojas de la vid. Cuando son al inicio de la brotación, provoca deformación de las hojas, acorta los entrenudos de los pámpanos y reduce el tamaño de los racimos.</p>
<p>En las hojas ya desarrolladas, aparecen decoloraciones amarillas (variedades blancas) o rojizas (variedades tintas) sin perjudicar a los nervios, pudiendo secar todo el limbo de la hoja. Si el ataque es muy severo puede provocar defoliación prematura.</p>
<p style="font-size:20px;">•	Establecimiento de umbral de referencia</p>
<p>Examinar 20 hojas al azar de la misma parcela, hasta el estado fenológico G (racimos separados) se tomará la segunda hoja más desarrollada. Entre el estado fenológico G y el J (cuajado), se tomarán hojas de la mitad inferior del sarmiento. A partir del estado fenológico K (grano tamaño guisante) se cogerán hojas de la parte media del sarmiento. Tratar en el caso de encontrar más de 10 hojas atacadas.</p>
<p style="font-size:20px;">•	Métodos de control</p>
<p><strong>Cultural:</strong> eliminar restos de poda y las hierbas del viñedo y alrededores, siendo la época del “lloro” el mejor momento. No abusar de abonos nitrogenados para reducir la fertilidad de las hembras. </p>
<p><strong>Biológico: </strong>empleando depredadores como los ácaros fitoseidos principalmente del género <i> Typhlodromus: Amblyseius swirskii</i> .</p>
<p><strong>Químico:</strong> el azufre espolvoreado da buenos resultados, se recomienda emplear productos químicos autorizados de forma específica en el Registro de productos fitosanitarios del MAPAMA</p>
