<h1 id="fitoplasma1">MADERA NEGRA (Stolbur phytoplasma)</h1>

<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p>Stolbur phytoplasma</p>
<p>El vector de este fitoplasma es el insecto <i>Hyalesthes obsoletus</i>.</p>
<p></p>
<p style="font-size:20px;">•	Agente productor de la plaga</p>
<p>El insecto vector de la enfermedad, es un homóptero, <i>Hyalesthes obsoletus</i>, presente en la cuenca Mediterránea y Norte de Europa.</p>
<p>El ciclo biológico la hembra pone los huevos en las plantas huéspedes en el verano, las larvas neonatas emigran hacia el suelo, las raíces, donde durante el invierno y primavera se desarrollan. Pasados 5 estados larvarios los adultos emergen a principios de verano y sobrevuelan las plantas.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>La enfermedad está influenciada por las condiciones climáticas y la variedad de vid.</p>
<p>En primavera se observa un retraso de la brotación, marchitamiento de las inflorescencias. Durante el envero el limbo de la hoja se endurece y vuelve frágil, el limbo presenta enrollamiento hacia el envés y escamas, con una coloración anormal de las hojas, de color amarillento-dorado en las variedades blancas, y de un rojo vivo en las tintas. Más tarde las hojas pueden presentar un color amarillo marcado a lo largo de los nervio con tendencia a necrosarse.</p>
<p>Las yemas apicales, brotes, inflorescencias y racimillos se secan. Las bayas se arrugan a partir del tallo, las bayas se secan completamente y adquieren un sabor agrio y amargo</p>
<p>Al final del verano, el síntoma más característico es la ausencia parcial o total de la lignificación de los sarmientos, los enfermos se ennegrecen.</p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p></p>
<p><strong> Cultural:</strong> preventivo, mediante el seguimiento y control de insectos vectores, empleo de material vegetal sano y certificado para nuevas plantaciones. </p>
<p></p>
<p></p>
<p><strong> Químico:</strong> como en el caso de las enfermedades virales de la vid, desafortunadamente no existe ningún método de control. Las vides jóvenes pueden morir a los dos o tres años. </p>
