<h1 id="fitoplasma1">FLAVESCENCIA DORADA</h1>

<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p>Phytoplasma</p>
<p>El vector de este fitoplasma es el insecto <i>Scaphoideus  titanus Ball</i>.</p>
<p></p>
<p style="font-size:20px;">•	Agente productor de la plaga</p>
<p>El insecto vector de la enfermedad, es una cicadella, <i>Scaphoideus  titanus</i>, presente en la cuenca Mediterránea y Norte de Europa.</p>
<p>Tiene una  sola  generación  anual  y  su  único huésped es la viña.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>Los síntomas que manifiestan las plantas afectadas de flavescencia dorada y de madera negra son similares, aunque pueden mostrarse de forma distinta según las variedades.  Es  prácticamente  imposible  diferenciarlos a simple vista y solamente el diagnóstico de un laboratorio especializado puede hacerlo.</p>
<p>En verano se observa un prematuro cambio de color de las hojas: amarillo en las variedades blancas y rojizo en las variedades tintas. Acompañado de enrollamiento de las hojas hacia el envés y falta de agostamiento en los sarmientos y desacación de los racimos.</p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p></p>
<p><strong> Cultural:</strong> preventivo, mediante el seguimiento y control de insectos vectores, empleo de material vegetal sano y certificado para nuevas plantaciones. </p>
<p></p>
<p></p>
<p><strong> Químico:</strong> como en el caso de las enfermedades virales de la vid, no existe ningún método de control. </p>

