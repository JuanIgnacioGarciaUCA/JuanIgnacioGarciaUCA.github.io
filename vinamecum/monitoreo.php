<h1>Monitoreo y trampas</h1>
<p>Trampas para el seguimiento masivo y/o monitoreo de diversas plagas de insectos en el viñedo, siendo de ayuda para saber qué tipo y umbral de plagas puede haber.</p>
<p></p>
<p style="font-size:20px;">•	Trampa Delta</p>
<p>Las trampas Delta se usan con la feromona sexual correspondiente para el monitoreo de plagas, de tal manera que el insecto se sentirá atraído por el olor que desprende la feromona y quedará pegado en el fondo engomado, pudiéndose realizar el conteo del mismo.</p>
<p>Constituida de plástico ondulado de polipropileno celular, plegable y resistente a las inclemencias climáticas. Se monta dejando la parte de abajo en plano, con un fondo engomado y con la feromona específica de cada insecto en una cápsula situada en el centro, se doblan las lengüetas laterales hacia dentro de las ranuras, se cierran los laterales y se cuelga colocándola a la altura de los racimos. El número de trampas a emplear será en función de la recomendación de la casa comercial.</p>
<p></p>
<img class="w3-image" src="imagenes trampas delta/1.jpg">
<p></p>

<p style="font-size:20px;">•	Trampas Tiras Adhesivas</p>
<p>Tiras adhesivas engomadas, a las que se puede añadir la feromona sexual de la especie específica a capturar, las feromona atrae a los machos adultos quedando adheridos a la tira, facilitando su observación, conteo e identificación.</p>
<p></p>
<img class="w3-image" src="imagenes trampas delta/9.jpg">
<p></p>
<p style="font-size:20px;">•	Trampas Cromáticas</p>
<p>Las trampas cromáticas se basan en la atracción de ciertos insectos a determinados colores, capturando adultos en vuelo para monitorear y detectar la presencia de la plaga, o para capturar masivamente al insecto y disminuir las poblaciones de la plaga en el viñedo. </p>
<p>Las trampas azules atraen principalmente a los Trips, las amarillas atraen a los Dípteros (moscas) y Áfidos (pulgones).</p>
<p>Este tipo de trampa, en placas o en rollos adhesivos, están elaboradas de un plástico rígido y resistente, cubierto por ambas caras de un pegamento entomológico donde el insecto se quedará atrapado, normalmente son repelentes al agua y que no deben de deteriorarse con altas temperaturas. </p>
<p></p>
<img class="w3-image" src="imagenes trampas delta/2.jpg">
<p></p>
<p style="font-size:20px;">•	Polilleros</p>
<p>Trampas diseñadas para la captura de polillas y mariposas, las cuales están cebadas con la feromona específica de cada insecto. Fundamentalmente se emplean para la captura masiva de insectos y reducir su población.</p>
<p>Constituida por una unidad desmontable: una base de plástico, embudo, tapa con el portaferomona de la especia a capturar, tapón y enganches para colgarla. </p>
<p></p>
<img class="w3-image" src="imagenes trampas delta/3.jpg">
<p></p>
<p style="font-size:20px;">•	Mosqueros</p>
<p>Son trampas especialmente diseñadas para la captura de Dípteros (mosca de la fruta), los cuales están cebados con el atrayente según el tipo de Díptero, pudiéndose emplear conjuntamente una pastilla insecticida, cola o agua jabonosa para evitar el escape de las moscas capturadas.</p>
<p>Constituida por una unidad desmontable: una base de plástico, embudo, tapa con el portaferomona de la especia a capturar, tapón y enganches para colgarla. </p>
<p></p>
<img class="w3-image" src="imagenes trampas delta/4.jpg" width="50%">
<p></p>
<p style="font-size:20px;">•	Difusores de Confusión Sexual</p>
<p>Se basa en la difusión homogénea al ambiente de feromonas sexuales sintéticas del insecto que se desea controlar, ya que imitan la composición molecular de las hormonas de la hembra atrayendo al macho y confundiéndolo, el cual no podrá localizar a la hembra, impidiéndose así el acoplamiento y evitar la siguiente generación de polillas.</p>
<p>Hay dos tipos:</p>
<p>Emisión pasiva (difusores), es el sistema más extendido, mediante pequeños difusores que contienen la feromona, diseñados con un plástico de una estructura química específica que libera la feromona. Hay dos diseños, uno tipo mosquetón, y otro alámbrico constituido por dos tubos microcapilares paralelos, de material polimérico de color rojizo, uno con un hilo de aluminio diseñado para ser torcido en los brotes de la vid y el otro con el tubo lleno de la feromona específica del insecto. Suelen ser envases que se venden en paquetes. </p>
<p>Emisión activa (sistema Puffer): Este tipo de difusor consiste en un dispositivo electrónico automático, programable, con un aerosol incorporado que contiene la feromona, la cual es emitida al ambiente a la frecuencia que se establezca. Es conveniente programarlo por la noche coincidiendo con el periodo de actividad reproductiva del insecto.</p>
