<p style="font-size:20px;">Recomendación</p>
<p>Para maximizar la eficacia de todas las trampas en una zona determinada, es conveniente que los tratamientos sean realizados a la vez por todos los agricultores vecinos.</p>
<p>Las trampas han de tener la suficiente carga de feromona para llegar al final del periodo vegetativo.</p>
<p>La difusión ha de ser homogénea y constante en toda la superficie de la parcela, además de reforzar los bordes de las parcelas, tanto internos como externos (casas, carreteras, bosques, etc.).</p>
