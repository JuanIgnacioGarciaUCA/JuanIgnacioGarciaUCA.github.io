<h1 id="virus2">ENTRENUDO CORTO (Virus GFLV)</h1>
<p></p>
<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p>Virus GFLV (grapevine fanleaf virus)</p>
<p>De estructura poliédrica, tamaño 30 nanómetros, formados por un filamento de ARN, rodeado de una capa polipéptida, y se transmite por nematodos.</p>
<p><p>
<p style="font-size:20px;">•	Transmisión</p>
<p>Transmitida durante la multiplicación vegetativa con plantas infectadas, o injertando un patrón sano con una variedad infectada, o al revés. </p>
<p>También puede transmitirse a través de los nematodos, Xiphinema index Thorne & Allen y Xiphinema italiae Meyl, ya que el virus se encuentra dentro del grupo de estos nematodos (Nepovirus). </p>
<p>No es transmisible con las herramientas de poda ni aperos agrícolas.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>Los síntomas en el viñedo aparecen aleatoriamente, a partir de focos más o menos grandes.</p>
<p>La planta presenta un debilitamiento general, sin síntomas claros, acortado la vida útil de la planta. Las hojas presentan un característico mosaico blanco o amarillo, pudiendo estar acompañado de una deformación, con los senos y los dientes de la hoja menos marcados. </p>
<p>Los sarmientos tienen problemas de desarrollo, el entrenudo situado en el 6º - 7º nudo queda corto, aparecen bifurcaciones, se aplanan o muestran crecimiento en zig-zag.</p>
<p>Los zarcillos se decoloran, se altera la fertilidad de la cepa, pudiendo manifestar corrimiento o caída del racimillo. Puede verse afectada la correcta maduración de los racimos, la calidad y el rendimiento.</p>
<p>Las raíces pueden sufrir crecimiento y morir.</p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p></p>
<p><strong> Cultural:</strong>  el control solo puede ser preventivo, mediante el uso de material sano en nuevas plantaciones, barbecho y desinfección del terreno, uso de portainjertos resistentes a nematodos. </p>
<p>Determinadas prácticas del cultivo pueden mejorar los síntomas de la enfermedad, como aplicación de quelatos, abonos nitrogenados, etc.</p>
<p></p>
<p><strong> Químico:</strong>  no hay lucha química directa para combatir la enfermedad. </p>
