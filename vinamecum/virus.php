<h1 id="virus1">ENROLLADO DE LA VID ( Virus GLRaV-I y GLRaV-III)</h1>
<p></p>
<p style="font-size:20px;">•	Agente productor de la enfermedad</p>
<p>Cinco tipos de virus, que se distinguen por números, los más graves son el 1 y el 3 (GLRaV-I y GLRaV-III) pertenecen al grupo Closterovirus, con un tamaño de 1400 a 2200 nanómetros.</p>
<p></p>
<p style="font-size:20px;">•	Transmisión</p>
<p>Transmitida durante la multiplicación vegetativa con plantas infectadas, estando también demostrada la transmisión por cochinillas Planeococcus longispinus y Planococcus ficus entre otras.</p>
<p></p>
<p style="font-size:20px;">•	Sintomatología y daños</p>
<p>Los síntomas de ambos virus son similares. Atacan los vasos con la savia elaborada, impidiendo que el azúcar producido en las hojas no llegue al resto de la planta. </p>
<p>Hojas: Se produce un enrollamiento de las hojas hacia el envés (cara inferior). </p>
<p>En las variedades blancas se produce una coloración amarillenta, difícil de diferenciar de otros problemas de la vid, pudiendo pasar desapercibida. El periodo de incubación dura cerca de meses. </p>
<p>En variedades tintas las hojas se vuelven rojizas debido a la acumulación de antocianos, mientras que los nervios principales permanecen verdes. AL final de verano las hojas se secan y necrosan.</p>
<p>En general, los virus reducen la fertilidad y el vigor de la cepa, provoca un retraso de la maduración de las bayas, afectando al desarrollo de los polifenoles. La intensidad de los síntomas varía según las variedades de uva, el virus y las condiciones climáticas. El virus también puede ser responsable de la incompatibilidad de la variedad aérea con el portainjerto.</p>
<p>En las raíces se produce una disminución de tamaño y cantidad. </p>
<p>Es aconsejable para un correcto diagnóstico confirmar la enfermedad con análisis de laboratorios especializados (test ELISA o Pruebas PCR).</p>
<p></p>
<p style="font-size:20px;">•	Medios de control</p>
<p></p>
<p><strong> Cultural:</strong> la forma preventiva es realizar plantaciones con material sano certificado, tanto en las reposiciones como en nuevas plantaciones. Eliminar las cepas afectadas. </p>
<p>Seguimiento y control de vectores: cochinillas (<i>Planococcus citri</i>, <i>P. ficus, Pseudococcus longispinus</i>) portadoras del virus mediante mosqueros o trampas pegajosas</p>
<p></p>
<p><strong> Químico:</strong>  desafortunadamente es una enfermedad incurable, no existen tratamientos químicos.</p>
